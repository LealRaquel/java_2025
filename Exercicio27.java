public class Exercicio27 {
    public static void main(String[] args) {
        String idade = "18";

        //Converter a string para o int
        int idade2 = Integer.parseInt(idade);
        System.out.println("Valor Int: " + idade2);

        //Converter o int para o float
        float idade3 = Float.parseFloat(idade);
        System.out.println("valor Float: " + idade3);

    }
    
}
